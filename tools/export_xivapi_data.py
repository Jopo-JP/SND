def run_export(args: argparse.Namespace) -> None:
    requested = {
        "items": parse_id_spec(args.items),
        "bnpc_names": parse_id_spec(args.bnpc_names),
        "territories": parse_id_spec(args.territories),
        "maps": parse_id_spec(args.maps),
        "place_names": parse_id_spec(args.place_names),
        "bnpc_bases": parse_id_spec(args.bnpc_bases),
    }

    selected = {type_name: ids for type_name, ids in requested.items() if ids}
    if not selected:
        raise ExportError("No IDs provided. Use e.g. --items 12628 or --mobs 3471.")

    cache = load_cache(args.output_dir)

    for type_name, ids in selected.items():
        export_type(
            type_name,
            ids,
            args.output_dir,
            cache,
            replace=args.replace,
            refresh=args.refresh,
            retry_missing=args.retry_missing,
        )

    save_cache(args.output_dir, cache)


def run_missing(args: argparse.Namespace) -> None:
    cache = load_cache(args.output_dir)
    type_name = normalize_type(args.type) if args.type else None
    print_missing(cache, type_name)


def run_retry_missing(args: argparse.Namespace) -> None:
    cache = load_cache(args.output_dir)
    type_name = normalize_type(args.type) if args.type else None
    selected = missing_ids(cache, type_name)
    if not selected:
        print("No known missing IDs to retry.")
        return

    for current_type, ids in selected.items():
        export_type(
            current_type,
            ids,
            args.output_dir,
            cache,
            replace=False,
            refresh=False,
            retry_missing=True,
        )

    save_cache(args.output_dir, cache)


def main(argv: list[str] | None = None) -> int:
    parser = build_parser()
    args = parser.parse_args(argv)
    try:
        if args.command == "export":
            run_export(args)
        elif args.command == "missing":
            run_missing(args)
        elif args.command == "retry-missing":
            run_retry_missing(args)
        elif args.command == "search":
            search(normalize_type(args.type), args.query, args.language, args.limit)
        else:
            parser.error(f"Unknown command {args.command}")
    except (ExportError, ValueError) as exc:
        print(f"error: {exc}", file=sys.stderr)
        return 1
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
