#!/usr/bin/env python3
"""Export selected XIVAPI v2 rows as Lua data files.

This script is intentionally dependency-free. It runs outside SND, resolves and
validates game data through XIVAPI, then writes small Lua lookup tables that SND
can require without doing HTTP requests on the game thread.
"""

from __future__ import annotations

import argparse
import json
import sys
import time
from datetime import datetime, timezone
from pathlib import Path
from typing import Any
from urllib.error import HTTPError, URLError
from urllib.parse import quote, urlencode
from urllib.request import Request, urlopen


BASE_URL = "https://v2.xivapi.com/api"
LANGUAGES = ("en", "de", "fr", "ja")
ROOT = Path(__file__).resolve().parents[1]
DEFAULT_OUTPUT_DIR = ROOT / "data" / "generated"
CACHE_FILENAME = "_xivapi_export_cache.json"


class ExportError(RuntimeError):
    pass


class MissingData(Exception):
    def __init__(self, status: str, message: str):
        super().__init__(message)
        self.status = status


def parse_id_spec(value: str) -> list[int]:
    """Parse comma-separated IDs and inclusive ranges like '1,5,50-80'."""
    ids: set[int] = set()
    if not value:
        return []

    for raw_part in value.split(","):
        part = raw_part.strip()
        if not part:
            continue

        if "-" in part:
            start_raw, end_raw = part.split("-", 1)
            start = int(start_raw.strip())
            end = int(end_raw.strip())
            if start > end:
                raise ExportError(f"Invalid range '{part}': start is greater than end")
            ids.update(range(start, end + 1))
        else:
            ids.add(int(part))

    return sorted(ids)


def lua_escape(value: Any) -> str:
    text = "" if value is None else str(value)
    return (
        text.replace("\\", "\\\\")
        .replace("\n", "\\n")
        .replace("\r", "\\r")
        .replace('"', '\\"')
    )


def lua_value(value: Any, indent: int = 0) -> str:
    if value is None:
        return "nil"
    if isinstance(value, bool):
        return "true" if value else "false"
    if isinstance(value, (int, float)):
        return str(value)
    if isinstance(value, str):
        return f'"{lua_escape(value)}"'
    if isinstance(value, list):
        if not value:
            return "{}"
        spaces = " " * indent
        child = " " * (indent + 4)
        lines = ["{"]
        for item in value:
            lines.append(f"{child}{lua_value(item, indent + 4)},")
        lines.append(f"{spaces}}}")
        return "\n".join(lines)
    if isinstance(value, dict):
        if not value:
            return "{}"
        spaces = " " * indent
        child = " " * (indent + 4)
        lines = ["{"]
        if set(value).issubset(set(LANGUAGES)):
            items = [(key, value[key]) for key in LANGUAGES if key in value]
        else:
            items = list(value.items())
        for key, item in items:
            if isinstance(key, int):
                key_repr = f"[{key}]"
            elif isinstance(key, str) and key.isidentifier() and not key.startswith("__"):
                key_repr = key
            else:
                key_repr = f'["{lua_escape(key)}"]'
            lines.append(f"{child}{key_repr} = {lua_value(item, indent + 4)},")
        lines.append(f"{spaces}}}")
        return "\n".join(lines)
    return f'"{lua_escape(value)}"'


def write_lua_table(path: Path, rows: dict[int, dict[str, Any]], meta: dict[str, Any]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    lines = [
        "-- ======================================================================",
        "-- Generated by tools/export_xivapi_data.py - do not edit manually.",
        "-- ======================================================================",
        "local data = {",
        f"    _meta = {lua_value(meta, 4)},",
    ]

    for row_id in sorted(rows):
        lines.append(f"    [{row_id}] = {lua_value(rows[row_id], 4)},")

    lines.extend(["}", "", "return data", ""])
    path.write_text("\n".join(lines), encoding="utf-8")


def load_cache(output_dir: Path) -> dict[str, dict[str, dict[str, Any]]]:
    path = output_dir / CACHE_FILENAME
    if not path.exists():
        return {}
    with path.open("r", encoding="utf-8") as handle:
        data = json.load(handle)
    return data if isinstance(data, dict) else {}


def save_cache(output_dir: Path, cache: dict[str, dict[str, dict[str, Any]]]) -> None:
    output_dir.mkdir(parents=True, exist_ok=True)
    path = output_dir / CACHE_FILENAME
    path.write_text(
        json.dumps(cache, ensure_ascii=False, indent=2) + "\n",
        encoding="utf-8",
    )


def missing_cache(cache: dict[str, Any]) -> dict[str, dict[str, dict[str, Any]]]:
    missing = cache.setdefault("_missing", {})
    if not isinstance(missing, dict):
        cache["_missing"] = {}
        missing = cache["_missing"]
    return missing


def get_missing(cache: dict[str, Any], type_name: str, row_id: int) -> dict[str, Any] | None:
    type_missing = missing_cache(cache).get(type_name, {})
    if not isinstance(type_missing, dict):
        return None
    entry = type_missing.get(str(row_id))
    return entry if isinstance(entry, dict) else None


def set_missing(cache: dict[str, Any], type_name: str, row_id: int, status: str, message: str) -> None:
    missing = missing_cache(cache)
    type_missing = missing.setdefault(type_name, {})
    type_missing[str(row_id)] = {
        "id": row_id,
        "status": status,
        "message": message,
        "last_checked_at": datetime.now(timezone.utc).replace(microsecond=0).isoformat(),
    }


def clear_missing(cache: dict[str, Any], type_name: str, row_id: int) -> None:
    missing = missing_cache(cache)
    type_missing = missing.get(type_name)
    if isinstance(type_missing, dict):
        type_missing.pop(str(row_id), None)
        if not type_missing:
            missing.pop(type_name, None)


def missing_ids(cache: dict[str, Any], type_name: str | None = None) -> dict[str, list[int]]:
    missing = missing_cache(cache)
    type_names = [type_name] if type_name else sorted(k for k in missing if k in EXPORTERS)
    out: dict[str, list[int]] = {}
    for current_type in type_names:
        type_missing = missing.get(current_type, {})
        if isinstance(type_missing, dict):
            ids = sorted(int(row_id) for row_id in type_missing if str(row_id).isdigit())
            if ids:
                out[current_type] = ids
    return out


def http_json(path: str, params: dict[str, Any] | None = None, retries: int = 2) -> dict[str, Any] | None:
    url = f"{BASE_URL}{path}"
    if params:
        url = f"{url}?{urlencode(params, safe=',.~()@[]')}"

    for attempt in range(retries + 1):
        try:
            request = Request(url, headers={"User-Agent": "snd-xivapi-exporter/1.0"})
            with urlopen(request, timeout=30) as response:
                return json.loads(response.read().decode("utf-8"))
        except HTTPError as exc:
            if exc.code == 404:
                raise MissingData("not_found", f"HTTP 404 for {url}") from exc
            if attempt >= retries:
                raise ExportError(f"HTTP {exc.code} for {url}") from exc
        except (URLError, TimeoutError, json.JSONDecodeError) as exc:
            if attempt >= retries:
                raise ExportError(f"Request failed for {url}: {exc}") from exc
        time.sleep(0.4 * (attempt + 1))

    return None


def lang_fields(field: str) -> str:
    return ",".join(f"{field}@lang({lang})" for lang in LANGUAGES)


def nested_lang_fields(prefix: str, field: str) -> str:
    return ",".join(f"{prefix}.{field}@lang({lang})" for lang in LANGUAGES)


def extract_langs(fields: dict[str, Any], field: str) -> dict[str, str]:
    return {lang: fields.get(f"{field}@lang({lang})") or "" for lang in LANGUAGES}


def ref_id(value: Any) -> int | None:
    if isinstance(value, dict):
        row_id = value.get("row_id") or value.get("value")
        return int(row_id) if isinstance(row_id, int) else None
    if isinstance(value, int):
        return value
    return None


def field_id(fields: dict[str, Any], name: str) -> int:
    return ref_id(fields.get(f"{name}@as(raw)")) or ref_id(fields.get(name)) or 0


def fetch_sheet_row(sheet: str, row_id: int, fields: str) -> dict[str, Any] | None:
    encoded_sheet = quote(sheet, safe="")
    return http_json(f"/sheet/{encoded_sheet}/{row_id}", {"fields": fields})


def row_fields(row: dict[str, Any] | None, sheet: str, row_id: int) -> dict[str, Any]:
    fields = row.get("fields") if isinstance(row, dict) else None
    if not isinstance(fields, dict) or not fields:
        raise MissingData("empty", f"{sheet}/{row_id} returned empty fields")
    return fields


def fetch_item(row_id: int) -> dict[str, Any] | None:
    row = fetch_sheet_row("Item", row_id, lang_fields("Name"))
    fields = row_fields(row, "Item", row_id)
    names = extract_langs(fields, "Name")
    if not any(names.values()):
        raise MissingData("empty", f"Item/{row_id} has no localized Name")
    return {"id": row_id, "name": names}


def fetch_bnpc_name(row_id: int) -> dict[str, Any] | None:
    fields_query = f"{lang_fields('Singular')},{lang_fields('Plural')}"
    row = fetch_sheet_row("BNpcName", row_id, fields_query)
    fields = row_fields(row, "BNpcName", row_id)
    singular = extract_langs(fields, "Singular")
    if not any(singular.values()):
        raise MissingData("empty", f"BNpcName/{row_id} has no localized Singular")
    return {
        "id": row_id,
        "singular": singular,
        "plural": extract_langs(fields, "Plural"),
    }


def fetch_place_name(row_id: int) -> dict[str, Any] | None:
    row = fetch_sheet_row("PlaceName", row_id, lang_fields("Name"))
    fields = row_fields(row, "PlaceName", row_id)
    names = extract_langs(fields, "Name")
    if not any(names.values()):
        raise MissingData("empty", f"PlaceName/{row_id} has no localized Name")
    return {"id": row_id, "name": names}


def nested_langs(fields: dict[str, Any], ref_field: str, name_field: str = "Name") -> dict[str, str]:
    ref = fields.get(ref_field)
    if not isinstance(ref, dict):
        return {lang: "" for lang in LANGUAGES}
    nested_fields = ref.get("fields") or {}
    return extract_langs(nested_fields, name_field)


def fetch_territory(row_id: int) -> dict[str, Any] | None:
    fields_query = ",".join(
        [
            "Name",
            "Map@as(raw)",
            "PlaceName@as(raw)",
            "PlaceNameRegion@as(raw)",
            "PlaceNameZone@as(raw)",
            nested_lang_fields("PlaceName", "Name"),
            nested_lang_fields("PlaceNameRegion", "Name"),
            nested_lang_fields("PlaceNameZone", "Name"),
        ]
    )
    row = fetch_sheet_row("TerritoryType", row_id, fields_query)
    fields = row_fields(row, "TerritoryType", row_id)

    names = nested_langs(fields, "PlaceName")
    if not any(names.values()):
        raise MissingData("empty", f"TerritoryType/{row_id} has no localized PlaceName")

    result = {
        "id": row_id,
        "internal_name": fields.get("Name") or "",
        "name": names,
        "place_name_id": field_id(fields, "PlaceName"),
        "region_place_name_id": field_id(fields, "PlaceNameRegion"),
        "zone_place_name_id": field_id(fields, "PlaceNameZone"),
        "map_id": field_id(fields, "Map"),
        "region_name": nested_langs(fields, "PlaceNameRegion"),
        "zone_name": nested_langs(fields, "PlaceNameZone"),
    }
    return result


