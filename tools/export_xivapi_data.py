def normalize_type(value: str) -> str:
    key = value.strip().lower().replace("-", "_")
    if key not in ALIASES:
        valid = ", ".join(sorted(set(ALIASES)))
        raise ExportError(f"Unknown type '{value}'. Valid aliases: {valid}")
    return ALIASES[key]


def parse_types(value: str) -> list[str]:
    seen: set[str] = set()
    out: list[str] = []
    for part in value.split(","):
        if not part.strip():
            continue
        normalized = normalize_type(part)
        if normalized not in seen:
            out.append(normalized)
            seen.add(normalized)
    return out


def export_type(
    type_name: str,
    ids: list[int],
    output_dir: Path,
    cache: dict[str, Any],
    replace: bool,
    refresh: bool,
    retry_missing: bool,
) -> None:
    config = EXPORTERS[type_name]
    target = output_dir / config["filename"]
    if target.exists() and type_name not in cache and not replace:
        raise ExportError(
            f"{target} exists, but {CACHE_FILENAME} has no cached rows for {type_name}. "
            "Run again with --replace to rebuild this file."
        )
    cached_rows = {} if replace else cache.get(type_name, {})
    if not isinstance(cached_rows, dict):
        cached_rows = {}
    rows: dict[int, dict[str, Any]] = {int(row_id): row for row_id, row in cached_rows.items()}
    not_found: list[int] = []
    empty: list[int] = []
    skipped: list[int] = []
    skipped_missing: list[int] = []

    fetcher = config["fetch"]
    print(f"Exporting {type_name}: {len(ids)} requested IDs")
    for index, row_id in enumerate(ids, start=1):
        if row_id in rows and not replace and not refresh:
            clear_missing(cache, type_name, row_id)
            skipped.append(row_id)
            print(f"  [{index}/{len(ids)}] {type_name} ID {row_id} already cached - skipped")
            continue

        missing_entry = get_missing(cache, type_name, row_id)
        if missing_entry and not replace and not refresh and not retry_missing:
            skipped_missing.append(row_id)
            status = missing_entry.get("status", "missing")
            print(f"  [{index}/{len(ids)}] {type_name} ID {row_id} known {status} - skipped")
            continue

        print(f"  [{index}/{len(ids)}] {type_name} ID {row_id}")
        try:
            data = fetcher(row_id)
        except MissingData as exc:
            rows.pop(row_id, None)
            set_missing(cache, type_name, row_id, exc.status, str(exc))
            if exc.status == "not_found":
                not_found.append(row_id)
            elif exc.status == "empty":
                empty.append(row_id)
            else:
                empty.append(row_id)
            print(f"    warning: {exc.status}: {exc}")
            continue
        rows[row_id] = data
        clear_missing(cache, type_name, row_id)

    current_missing = missing_ids(cache, type_name).get(type_name, [])

    meta = {
        "generated_at": datetime.now(timezone.utc).replace(microsecond=0).isoformat(),
        "source": "XIVAPI v2",
        "type": type_name,
        "languages": list(LANGUAGES),
        "requested_ids": ids,
        "skipped_ids": skipped,
        "skipped_missing_ids": skipped_missing,
        "not_found_ids": not_found,
        "empty_ids": empty,
        "missing_ids": current_missing,
        "failed_ids": not_found + empty,
        "count": len(rows),
    }
    write_lua_table(target, rows, meta)
    cache[type_name] = {str(row_id): rows[row_id] for row_id in sorted(rows)}
    print(
        f"  wrote {display_path(target)} "
        f"({len(rows)} rows, {len(skipped)} cached, {len(skipped_missing)} known missing, "
        f"{len(not_found)} not found, {len(empty)} empty)"
    )


def display_path(path: Path) -> str:
    try:
        return str(path.relative_to(ROOT))
    except ValueError:
        return str(path)


def search(type_name: str, query: str, language: str, limit: int) -> None:
    if type_name not in SEARCHABLE_TYPES:
        raise ExportError(f"{type_name} is exportable by ID, but not searchable by text")
    languages = LANGUAGES if language == "all" else (language,)
    config = EXPORTERS[type_name]
    search_field = config["search_field"]
    query_expr = f'{search_field}~"{query}"'

    results_by_id: dict[int, tuple[str, dict[str, Any]]] = {}
    for lang in languages:
        params = {
            "sheets": config["sheet"],
            "query": query_expr,
            "fields": config["search_fields"],
            "language": lang,
            "limit": limit,
        }
        data = http_json("/search", params)
        for result in data.get("results", []) if data else []:
            row_id = result.get("row_id")
            if isinstance(row_id, int) and row_id not in results_by_id:
                results_by_id[row_id] = (lang, result)

    results = sorted(results_by_id.values(), key=lambda item: item[1].get("score", 0), reverse=True)

    print(f"Search {type_name} for '{query}' ({language}): {len(results)} result(s)")
    for lang, result in results:
        row_id = result.get("row_id")
        score = result.get("score", 0)
        fields = result.get("fields") or {}
        label = search_label(type_name, fields)
        print(f"  {row_id:<7} lang={lang} score={score:.3f}  {label}")


def search_label(type_name: str, fields: dict[str, Any]) -> str:
    if type_name == "items":
        return str(fields.get("Name") or "")
    if type_name == "bnpc_names":
        return str(fields.get("Singular") or "")
    if type_name in ("territories", "maps"):
        place = fields.get("PlaceName")
        if isinstance(place, dict):
            place_fields = place.get("fields") or {}
            name = place_fields.get("Name") or ""
        else:
            name = ""
        extra = []
        map_ref = fields.get("Map@as(raw)") or fields.get("Map")
        territory_ref = fields.get("TerritoryType@as(raw)") or fields.get("TerritoryType")
        if ref_id(map_ref):
            extra.append(f"map={ref_id(map_ref)}")
        if ref_id(territory_ref):
            extra.append(f"territory={ref_id(territory_ref)}")
        suffix = f" ({', '.join(extra)})" if extra else ""
        return f"{name}{suffix}"
    if type_name == "place_names":
        return str(fields.get("Name") or "")
    if type_name == "bnpc_bases":
        rank = fields.get("Rank") or 0
        scale = fields.get("Scale") or 0
        model_chara_id = ref_id(fields.get("ModelChara@as(raw)")) or 0
        return f"rank={rank}, scale={scale}, model_chara_id={model_chara_id}"
    return str(fields)


def print_missing(cache: dict[str, Any], type_name: str | None = None) -> None:
    missing = missing_cache(cache)
    selected = missing_ids(cache, type_name)
    if not selected:
        print("No known missing IDs.")
        return

    for current_type, ids in selected.items():
        print(f"{current_type}: {len(ids)} missing ID(s)")
        type_missing = missing.get(current_type, {})
        for row_id in ids:
            entry = type_missing.get(str(row_id), {})
            status = entry.get("status", "missing")
            checked = entry.get("last_checked_at", "?")
            message = entry.get("message", "")
            print(f"  {row_id:<7} {status:<10} {checked}  {message}")


